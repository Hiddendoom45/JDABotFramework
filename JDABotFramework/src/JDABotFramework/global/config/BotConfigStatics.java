package JDABotFramework.global.config;
/**
 * Basic static values needed by the global config for a bot
 * @author Allen
 *
 */
public class BotConfigStatics {
	/**
	 * Default prefix for commands
	 */
	public String prefix = "-!";
	/**
	 * Default mod prefix for commands
	 */
	public String modPrefix = "~!";
	/**
	 * ID for the owner, set so that the owner can access all bot functionalities regardless of permissions
	 */
	public String ownerID = "0";
}
